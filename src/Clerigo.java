public class Clerigo {

    String nome;
    int vida;
    int ataque;
    int defesa;
    int fe;
    PoderDivino poderDivino;

    public Clerigo(String nome, int vida, int ataque, int defesa, int fe, PoderDivino poderDivino) {
        this.nome = nome;
        this.vida = vida;
        this.ataque = ataque;
        this.defesa = defesa;
        this.fe = fe;
        this.poderDivino = poderDivino;
    }
}