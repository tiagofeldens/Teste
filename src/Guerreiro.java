public class Guerreiro {

    String nome;
    int vida;
    int ataque;
    int defesa;
    Arma arma;


    public Guerreiro(String nome, int vida, int ataque, int defesa, Arma arma) {
        this.nome = nome;
        this.vida = vida;
        this.ataque = ataque;
        this.defesa = defesa;
        this.arma = arma;

    }
}