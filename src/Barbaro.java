public class Barbaro {

    String nome;
    int vida;
    int ataque;
    int defesa;
    Arma arma;

    public Barbaro(String nome, int vida, int ataque, int defesa, Arma arma) {
        this.nome = nome;
        this.vida = vida;
        this.ataque = ataque;
        this.defesa = defesa;
        this.arma = arma;
    }
}